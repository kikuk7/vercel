export const getPages = () => pages;

export const findPageById = (id) => pages.find(p => p.id === parseInt(id));

export const findPageBySlug = (slug) => pages.find(p => p.slug === slug);


export const updatePage = (id, updatedFields) => {
  const index = pages.findIndex(p => p.id === parseInt(id));
  if (index !== -1) {
    pages[index] = { ...pages[index], ...updatedFields };
    return pages[index];
  }
  return null;
};

export const createPage = (newPage) => {
  const newId = Math.max(...pages.map(p => p.id)) + 1;
  const page = { id: newId, ...newPage };
  pages.push(page);
  return page;
};

export const deletePage = (id) => {
  const initialLength = pages.length;
  pages = pages.filter(p => p.id !== parseInt(id));
  return pages.length < initialLength; // Return true if a page was deleted
};